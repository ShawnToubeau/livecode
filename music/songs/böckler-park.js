setCps(140 / 60 / 4);

fill$: s("vec1_sounds:117")
  .chop(140)
  .loopAt(1/2)
  .dist(1.5)
  .lpf(saw.range(400, 1200).fast(4))
  .coarse(2)
  .gain(slider(0.25, 0.15, 0.4, .01))
  .clip(1)
  ._scope()

bass$: note("c2".add("<0 1>/2").seg(8))
  .s("sbass:1")
  .begin(sine.range(0.15,.4))
  .dist(3)
  .gain("{.4 1.3}%8")
  .struct("{[1 ~ 1 ~] [1 ~ 1 1]}%4") 
  .lpf(100)
  .hpf(250)
  .clip(1)
  .velocity(sine.range(1.1, 1.4).fast(2))
  ._scope()

drum$: note("{b1 d2}%4")
  .s("vec1_punchy:11")
  .begin("{0.025 .05}%4")
  .crush(8)
  .struct("{1 0}%8") 
  .sometimesBy(.3, n => n.ply("2"))
  .lpf(2000)
  .clip(1)
  .velocity(sine.range(1.3, 1.6).fast(4))
  .postgain(1.4)
  ._scope()

hat$: s("vec1_open_hh:81!4")
  .sometimesBy(.1, ply("2"))
  .lpf(1200)
  .end(.90)
  ._scope()


synth$: note("{c2 <f1 g1>}".add("[7 12]/2"))
  .struct("{x!3 ~ x ~ x}%16")
  .s("supersaw")
  .ftype(2) // 1 | 2
  .lpf(slider(1780, 1000, 2000, 10))
  .lpenv(slider(8, 1, 10, 1))
  .lpq(slider(4, 1, 10, 1))
  .dist(slider(1.4, .5, 1.5, .05))
  ._scope()

